#include <stdio.h>

int main() {
    int numero = 10;
    char caracter = 'A';
    float flotante = 3.14;

    printf("El valor de la variable 'numero' es: %d\n", numero);
    printf("El valor de la variable 'caracter' es: %c\n", caracter);
    printf("El valor de la variable 'flotante' es: %.2f\n", flotante);

    return 0;
}
